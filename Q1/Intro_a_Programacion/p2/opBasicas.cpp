#include <iostream>
using namespace std;

int main(){
	float num1, num2;
	cout << "Operaciones basicas con 2 numeros.\nNumero 1: ";
	cin >> num1;
	cout << "Numero 2: ";
	cin >> num2;
	cout << "Suma: " << num1 + num2 << endl;
	cout << "Resta: " << num1 - num2 << endl;
	cout << "Multipliacion: " << num1 * num2 << endl;
	cout << "Divsion: " << num1 / num2 << endl;
	return 0;
}
